using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.Http;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.FileProviders;
using Microsoft.Extensions.Hosting;
using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;
using System.Threading.Tasks;

namespace MOVIEAPP
{
    public class Startup
    {
        // This method gets called by the runtime. Use this method to add services to the container.
        // For more information on how to configure your application, visit https://go.microsoft.com/fwlink/?LinkID=398940
        public void ConfigureServices(IServiceCollection services)
        {
            services.AddMvc().SetCompatibilityVersion(Microsoft.AspNetCore.Mvc.CompatibilityVersion.Version_2_2);
        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
        {
            //properties / louncsetting dosyasýnda devolopment özelliði varsa hata sayfalarýný gösterir diðer türlü göstermez
            //uygulama yayýnladýktan sonra bunlrý göstermeye gerek yok yayýnlarken devolopment kýsmýný productiona çevirmek gerek
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }


            app.UseStaticFiles(); //varsayýlan olarak wwwroot klasörümüz dýþarýya açýlmýþ olur yol => /css/style.css /img/1.jpeg
            // node_modules klasörünü dýþarýya açmak için bu kýsmý yazýyoruz 
            app.UseStaticFiles(new StaticFileOptions {
            FileProvider= new PhysicalFileProvider(Path.Combine
            ( Directory.GetCurrentDirectory(),"node_modules")),
               RequestPath="/modules"   //modules/bootstrap/dist/css/bootstrap.min.css þeklinde bir yol olacak
            });


            app.UseRouting();

            app.UseEndpoints(endpoints =>
            {

                endpoints.MapControllerRoute(
                    name: "default",
                    // action = controller içindeki her bir metoda veriðimiz isim
                    // id? = Soru iþareti isteðe baðlý olarak kullanýlacaðýný belirtir. 
                    pattern: "{controller=Home}/{action=Index}/{id?}");

                //url yolu course released year ve ay þeklinde olmasý lazým year ve month da ? olsaydý
                // year ve month alanlarýný doldurmaya gerek kalmayacaktý.
                endpoints.MapControllerRoute(
                    name: "CoursesByReleased",
                    pattern: "course/released/{id?}",
                    new { controller = "Movie", action = "Index" } 
                    );

                endpoints.MapControllerRoute(
                   name: "register",
                   pattern: "register/released/{id?}",
                   new { controller = "User", action = "Login" }
                   );

            });

        }
    }
}
